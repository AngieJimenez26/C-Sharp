namespace Programa05_01
{
    internal class Program
    {
        static void Main(string[] args)

        {
            double valor1 = 1.5;
            double valor2 = 4.7;
            double resultado = 0.0;


            // operaciones suma
            resultado = valor1 + valor2;
            Console.WriteLine("{0} + {1} = {2} APRENDÍ A HACER UNA SUMA PERROS", valor1, valor2, resultado);
            // RESTA
            resultado = valor1 - valor2;
            Console.WriteLine("{0} - {1} = {2} APRENDÍ A HACER UNA RESTA PERROS", valor1, valor2, resultado);

            // MULTIPLICACÍON
            resultado = valor1 * valor2;
            Console.WriteLine("{0} * {1} = {2} APRENDÍ A HACER UNA MULTIPLICACÍON PERROS", valor1, valor2, resultado);

            // DIVISIÓN
            resultado = valor1 / valor2;
            Console.WriteLine("{0} / {1} = {2} APRENDÍ A HACER UNA DIVISIÓN PERROS", valor1, valor2, resultado);

            // MODULO
            resultado = valor1 % valor2;
            Console.WriteLine("El residuo de {0} / {1} es {2} APRENDÍ A HACER UN RESIDUO DE LA DIVISIÓN PERROS", valor1, valor2, resultado);


            Console.WriteLine("MAMÁ ESTOY TRIUNFANDO");
            Console.WriteLine("BABIII ESTOY TRIUNFANDO =D");



        }
    }
}
